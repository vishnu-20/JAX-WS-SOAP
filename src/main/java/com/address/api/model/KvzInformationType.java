
package com.address.api.model;

public class KvzInformationType {

	private String identifier;

	private String postcode;

	private String city;

	private String street;

	private String housenumberForm;

	private String houseNumberAddition;

	private String loanDetails;

	public String getIdentifier() {
		return identifier;
	}

	public void setIdentifier(String identifier) {
		this.identifier = identifier;
	}

	public String getPostcode() {
		return postcode;
	}

	public void setPostcode(String postcode) {
		this.postcode = postcode;
	}

	public String getCity() {
		return city;
	}

	public void setCity(String city) {
		this.city = city;
	}

	public String getStreet() {
		return street;
	}

	public void setStreet(String street) {
		this.street = street;
	}

	public String getHousenumberForm() {
		return housenumberForm;
	}

	public void setHousenumberForm(String housenumberForm) {
		this.housenumberForm = housenumberForm;
	}

	public String getHouseNumberAddition() {
		return houseNumberAddition;
	}

	public void setHouseNumberAddition(String houseNumberAddition) {
		this.houseNumberAddition = houseNumberAddition;
	}

	public String getLoanDetails() {
		return loanDetails;
	}

	public void setLoanDetails(String loanDetails) {
		this.loanDetails = loanDetails;
	}
	
	

}
