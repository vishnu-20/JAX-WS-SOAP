
package com.address.api.model;

public class FlagType {

	private String flag;

	private String objectFlagPresentationTxt;

	public String getFlag() {
		return flag;
	}

	public void setFlag(String flag) {
		this.flag = flag;
	}

	public String getObjectFlagPresentationTxt() {
		return objectFlagPresentationTxt;
	}

	public void setObjectFlagPresentationTxt(String objectFlagPresentationTxt) {
		this.objectFlagPresentationTxt = objectFlagPresentationTxt;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	private String description;
	
	

}
